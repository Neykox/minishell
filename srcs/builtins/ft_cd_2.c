#include "../../includes/minishell.h"

char	*remove_eg(char *str)
{
	int		i;
	char	*tmp;

	i = 0;
	tmp = NULL;
	while (str[i] && str[i] != '=')
		i++;
	if (str[i] == '=')
		i++;
	tmp = ft_strdup(&str[i]);
	return (tmp);
}
